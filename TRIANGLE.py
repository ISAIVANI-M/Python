class triangle():
    def area():
        height=int(input("Height:"))
        breadth=int(input("Breadth:"))
        a=(height*breadth)/2
        print("Area of Triangle:", a)
        message="area"
        return message
    def perimeter():
        height1=int(input("Height1:"))
        height2=int(input("Height2:"))
        breadth=int(input("Breadth:"))
        p=(height1+height2+breadth)
        print("Perimeter of Triangle:", p)
        message="perimeter"
        return message
